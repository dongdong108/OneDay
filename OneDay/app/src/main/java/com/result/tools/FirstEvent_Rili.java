public class FirstEvent_Rili {
    private int year;
    private int day;
    private int month;

    public FirstEvent_Rili(int year, int day, int month) {
        this.year = year;
        this.day = day;
        this.month = month;
    }

    public int getMonth() {
        return month;
    }

    public int getYear() {
        return year;
    }

    public int getDay() {
        return day;
    }
}
